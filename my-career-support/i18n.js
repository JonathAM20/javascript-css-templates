/**
 * Translate component labels to english and french.
 * The elements are identified with `data-i18n` atribute.
 *
 * @param {string} lang - Language code (ex: 'fr' ou 'en').
 */
function changeLanguage(lang) {
    if (localStorage.getItem('currentLang') == lang) {
        console.warn(`The language is alreary "${lang}".`);
        return;
    }

    if (lang !== 'fr' && lang !== 'en') {
        console.warn(`Language "${lang}" has not support, 'en' language set as default.`);
        lang = 'en';
    }

    const elementsToTranslate = document.querySelectorAll('[data-i18n]');

    elementsToTranslate.forEach(element => {
        const key = element.getAttribute('data-i18n');

        if (key && translations[key] && translations[key][lang]) {
            const translation = translations[key][lang];
            element.innerHTML = translation;
        }
    });

    localStorage.setItem('currentLang', lang);
    console.log(`Language set to: ${lang}`);
}